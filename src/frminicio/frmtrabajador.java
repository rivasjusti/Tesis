    void mostrar(String buscar) {
        try {
            DefaultTableModel modelo;
            ftrabajador func = new ftrabajador();
            
            modelo = func.mostrar(buscar);
            
            jTListado.setModel(modelo);
            ocultar_columnas();
            jLRegistros.setText("Total Registros " + Integer.toString(func.totalregistros));
            
           

            //se le escribe el combo de estado donde hicimos   el frmestado haci hara para que llame al frmtrabajador al frmestado para guardar los estado en frmestado 
            //fmecanico ef = new fmecanico();
            f_tipoacceso ftp = new f_tipoacceso();
            f_estados tdf = new f_estados();
            f_departamento ft = new f_departamento();
            ftp.cargarComboTipoAcceso(jCBAcceso);
            tdf.cargarComboEstados(jCBEstado);
            ft.cargarComboDepartamento(jCBdepa);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

