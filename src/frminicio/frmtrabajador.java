    private void jTListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTListadoMouseClicked
        // TODO add your handling code here:
        jBGuardar.setText("Editar");
        habilitar();
        jBEliminar.setEnabled(true);
        accion = "editar";
        
        int fila = jTListado.rowAtPoint(evt.getPoint());
        
        jTFidpersona.setText(jTListado.getValueAt(fila, 0).toString());
        jTFNombre.setText(jTListado.getValueAt(fila, 1).toString());
        jTFApaterno.setText(jTListado.getValueAt(fila, 2).toString());
        jTFAmaterno.setText(jTListado.getValueAt(fila, 3).toString());
        jTFdire.setText(jTListado.getValueAt(fila, 4).toString());
        jTFteleofono.setText(jTListado.getValueAt(fila, 5).toString());
        jTFLogin.setText(jTListado.getValueAt(fila, 6).toString());
        jTFPassword.setText(jTListado.getValueAt(fila, 7).toString());
        jCBEstado.setSelectedItem(jTListado.getValueAt(fila, 8).toString());
        jCBAcceso.setSelectedItem(jTListado.getValueAt(fila, 9).toString());
        jCBdepa.setSelectedItem(jTListado.getValueAt(fila, 10).toString());
      
    }//GEN-LAST:event_jTListadoMouseClicked

