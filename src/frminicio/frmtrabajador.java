    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if (jTFNombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre del Trabajador");
            jTFNombre.requestFocus();
            return;
        }
        if (jTFApaterno.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Apellido del Trabajador");
            jTFApaterno.requestFocus();
            return;
        }
        if (jTFAmaterno.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Apellido del Trabajador");
            jTFAmaterno.requestFocus();
            return;
        }
        if (jTFdire.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Apellido del Trabajador");
            jTFdire.requestFocus();
            return;
        }
        if (jTFteleofono.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Apellido del Trabajador");
            jTFteleofono.requestFocus();
            return;
        }
        
        if (jTFLogin.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Acceso para el Trabajador");
            jTFLogin.requestFocus();
            return;
        }
        if (jTFPassword.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Password para el Trabajador");
            jTFPassword.requestFocus();
            return;
        }
        
        vtrabajador dts = new vtrabajador();
        ftrabajador func = new ftrabajador();
        f_tipoacceso ftp = new f_tipoacceso();
        f_departamento ft = new f_departamento();
        f_estados tdf = new f_estados();
        dts.setNombretra(jTFNombre.getText());
        dts.setApellidotra(jTFApaterno.getText());
          dts.setNumerodocumento(jTFAmaterno.getText());
        dts.setDireciontra(jTFdire.getText());
        dts.setTelefonotra(jTFteleofono.getText());
        dts.setLogin(jTFLogin.getText());
        dts.setPassword(jTFPassword.getText());
      
       int seleccionado = jCBEstado.getSelectedIndex();
        dts.setIdestado(tdf.obtenerIdEsado(jCBEstado));
        seleccionado = jCBAcceso.getSelectedIndex();
        dts.setIdTipoaceso(ftp.obtenerIdTipoacceso(jCBAcceso));
        
        seleccionado = jCBdepa.getSelectedIndex();
        dts.setIdDepartamento(ft.obtenerIdDepartamento(jCBdepa));
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Trabajador fue registrado satisfactoriamete!");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIdtipotrabajador(Integer.parseInt(jTFidpersona.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Trabajador fue editado satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

