    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if(txtcodigo.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el codigo");
            txtcodigo.requestFocus();
            return;
        }
         if(jTFNombre.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el Nombre");
            jTFNombre.requestFocus();
            return;
        }
        
        if(jTFmarca.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la marca  ");
            jTFmarca.requestFocus();
            return;
        }
        if(jTfcantidad.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la cantidad ");
            jTfcantidad.requestFocus();
            return;
        }
        if(jTFprecio.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar los precios ");
            jTFprecio.requestFocus();
            return;
        }
          
        vaccesorios dts = new vaccesorios();
        f_accesorios func = new f_accesorios();

        //dts.setCodigoaccesorios(Integer.parseInt(txtcodigo.getText()));
        dts.setCodigoaccesorios(txtcodigo.getText());
        dts.setNombreacc(jTFNombre.getText());
        dts.setMarcaacc(jTFmarca.getText());
        dts.setCantidadacc(Integer.parseInt(jTfcantidad.getText()));
        dts.setPrecioacc(Integer.parseInt(jTFprecio.getText()));

        if (accion.equals("guardar")) {
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "Accesorios fue registrado satisfactoriamete!");
                mostrar("");
                inhabilitar();
            }
        }
        else if (accion.equals("editar")) {
            dts.setIdAccesorios(Integer.parseInt(jTFidtipodoc.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Accesorios fue editado satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

