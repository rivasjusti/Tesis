    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        // TODO add your handling code here:
        if (!jTFidestado.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar el nombre Proveedor ?", "Confirmar", 2);

            if (confirmacion == 0) {
                f_provedor func = new f_provedor();
               vprovedor dts = new vprovedor();

                dts.setIdProovedor(Integer.parseInt(jTFidestado.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

