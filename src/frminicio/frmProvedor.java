    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if(jTFestado.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre del Proveedor");
            jTFestado.requestFocus();
            return;
        }
        if(jTFtele.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el telefono");
            jTFtele.requestFocus();
            return;
        }
        if(jTFdireccion.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la direccecion ");
            jTFdireccion.requestFocus();
            return;
        }
        if(jTFruc.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar su ruc");
            jTFruc.requestFocus();
            return;
        }
        if(jTempresa.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la Empresa");
            jTempresa.requestFocus();
            return;
        }
        vprovedor dts = new vprovedor();
        f_provedor func = new f_provedor();
        Calendar cal;
        int d,m,a;
        
        cal = jDcalentario.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        
        dts.setFechaingresopro(new Date(a,m,d));

        
       // f_modelo fun = new f_modelo();
        f_departamento fu = new f_departamento();
       
            f_estados fe =new f_estados();

        dts.setNombrepro(jTFestado.getText());
        dts.setTelefonopro(jTFtele.getText());
        dts.setDirecionopro(jTFdireccion.getText());
        dts.setRucpro(jTFruc.getText());
        dts.setEmpresapro(jTempresa.getText());
         
         
        

       

        dts.setIdEstado(fe.obtenerIdEsado(jCestado));
        dts.setIdDepartamento(fu.obtenerIdDepartamento(jCdepa));

        if (accion.equals("guardar")) {
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, " El proveedor  fue registrado satisfactoriamete!");
                mostrar("");
                inhabilitar();
            }
        }
        else if (accion.equals("editar")) {
            dts.setIdProovedor(Integer.parseInt(jTFidestado.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El nombre del Proveedor fue editado satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

