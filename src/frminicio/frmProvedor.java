    private void jTListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTListadoMouseClicked
        // TODO add your handling code here:
        jBGuardar.setText("Editar");
        habilitar();
        jBEliminar.setEnabled(true);
        accion = "editar";

        int fila = jTListado.rowAtPoint(evt.getPoint());

        jTFidestado.setText(jTListado.getValueAt(fila, 0).toString());
        jTFestado.setText(jTListado.getValueAt(fila, 1).toString());
        jTFtele.setText(jTListado.getValueAt(fila, 2).toString());
        jTFdireccion.setText(jTListado.getValueAt(fila, 3).toString());
        jTFruc.setText(jTListado.getValueAt(fila, 4).toString());
        jDcalentario.setDate(Date.valueOf(jTListado.getValueAt(fila, 5).toString()));
        jTempresa.setText(jTListado.getValueAt(fila, 6).toString());
        jCdepa.setSelectedItem(jTListado.getValueAt(fila, 7).toString());
        jCestado.setSelectedItem(jTListado.getValueAt(fila, 0).toString());
       
    }//GEN-LAST:event_jTListadoMouseClicked

