    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if (txtidcliente.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar al cliente si..");
            txtidcliente.requestFocus();
            return;
        }
        if (txtidmecanico.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar al mecanico si..");
            txtidmecanico.requestFocus();
            return;
        }
        if (jTorden.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el orden de llegada");
            jTorden.requestFocus();
            return;
        }
        
        
       
       
        

        vllegada dts = new vllegada();
        f_llegada func = new f_llegada();

       
        f_modelo fp = new f_modelo();
       // f_mecanico fm = new f_mecanico();
        //f_codigocliente fc=new f_codigocliente();
         Calendar cal;
        int d,m,a;
        
        cal = jDcalentar.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        
        dts.setFechallega(new Date(a,m,d));
       
       


       
       
       
    dts.setIdClientes(Integer.parseInt(txtidcliente.getText()));
       
        dts.setIdmecanico(Integer.parseInt(txtidmecanico.getText()));
       dts.setOrdenllegada(jTorden.getText());
        
       
         int seleccionado = jCBMODELO.getSelectedIndex();
         dts.setIdModelo(fp.obtenerIdModelo(jCBMODELO));
        

       
       
        //dts.setIdAccesorios(fm.obtenerIdAccesorios(jCBacc ));
        //////////////////////////////////////////
        

         

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La Llegada fue registrada satisfactoriamete!");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIdllegada(Integer.parseInt(jTFidhabitacion.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La Llegada  fue editada satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

