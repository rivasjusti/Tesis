    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if(jTFestado.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre del cliente");
            jTFestado.requestFocus();
            return;
        }
        if(jTFapellido.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un apellido");
            jTFapellido.requestFocus();
            return;
        }
        if(jTFdireccion.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la direccecion del cliente");
            jTFdireccion.requestFocus();
            return;
        }
        if(jTFtelefono.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la direccecion del cliente");
            jTFtelefono.requestFocus();
            return;
        }
        if(jTcodigo.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el codigo del cliente");
            jTcodigo.requestFocus();
            return;
        }

        vcodigo_cliente dts = new vcodigo_cliente();
        f_codigocliente func = new f_codigocliente();
       // f_modelo fun = new f_modelo();
        f_departamento fu = new f_departamento();

        dts.setNombrecli(jTFestado.getText());
        dts.setApellidocli(jTFapellido.getText());
        dts.setDireccioncli(jTFdireccion.getText());
        dts.setTelefono(jTFtelefono.getText());
        dts.setCodigo_cliente(jTcodigo.getText());
         
         
        

       

        //int seleccionado = jCdepa.getSelectedIndex();
        dts.setIdDepartamento(fu.obtenerIdDepartamento(jCdepa));

        if (accion.equals("guardar")) {
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El nombre del cliente fue registrado satisfactoriamete!");
                mostrar("");
                inhabilitar();
            }
        }
        else if (accion.equals("editar")) {
            dts.setIdClientes(Integer.parseInt(jTFidestado.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El nombre del cliente fue editado satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

