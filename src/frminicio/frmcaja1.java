    void mostrar (String buscar){
        try {
            DefaultTableModel modelo;
            f_caja func = new f_caja();
            f_tipofactura ft = new f_tipofactura();
            f_modelo fm = new f_modelo();
            f_accesorios fa = new f_accesorios();
            f_tarjeta fta = new f_tarjeta();
           
           // ftiporeserva fun = new ftiporeserva();
            modelo = func.mostrar(buscar);
            
            jTListado.setModel(modelo);
            ocultar_columnas();
            jLRegistros.setText("Total Registros " + Integer.toString(func.totalregistros));
            ft.cargarComboFactura(jctipofactura);
            fm.cargarComboModelo(jCmodelo);
            fa.cargarComboaccesorios(jCaccesorios);
            fta.cargarCombotarjeta(jCtarjeta);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

