    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
       
        if(jTFidCliente.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un Cliente");
            jTFidCliente.requestFocus();
            return;
        }
        if(jTFdescri.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Precio de Alojamiento");
            jTFdescri.requestFocus();
            return;
        }
        if(jTFcan.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Precio de Alojamiento");
            jTFcan.requestFocus();
            return;
        }
        if(jTFprecio.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Precio de Alojamiento");
            jTFprecio.requestFocus();
            return;
        }
        if(jTFiva.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Precio de Alojamiento");
            jTFiva.requestFocus();
            return;
        }
        if(jTFsubtotal.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Precio de Alojamiento");
            jTFsubtotal.requestFocus();
            return;
        }
        if(jTFtotalpago.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Precio de Alojamiento");
            jTFtotalpago.requestFocus();
            return;
        }
        vcaja dts = new vcaja();
        f_caja func = new f_caja();
        f_tipofactura ft = new f_tipofactura();
        f_modelo fm = new f_modelo();
        f_accesorios fa = new f_accesorios();
        f_tarjeta fta = new f_tarjeta();
       

        //dts.setIdHabitacion(Integer.parseInt(jTFidaccesorios.getText()));
        dts.setIdClientes(Integer.parseInt(jTFidCliente.getText()));
        dts.setIdtipotrabajador(idUsuario);
        
       // int seleccionado = jCtarjeta.getSelectedIndex();
        dts.setIdTipoFactura(ft.obtenerIdFactura(jctipofactura));
        dts.setIdModelo(fm.obtenerIdModelo(jCmodelo));
        dts.setIdAccesorios(fa.obtenerIdAccesorios(jCaccesorios));
        dts.setIdtarjeta(fta.obtenerIdtarjeta(jCtarjeta));
       
        Calendar cal;
        int d,m,a;
        cal = jDventa.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setFechaventa(new Date(a,m,d));
        
        //dts.setIdestados(fe.obtenerIdestados(jCBEstado));
        dts.setCant(Integer.parseInt(jTFcan.getText()));
        dts.setPrecio(Integer.parseInt(jTFprecio.getText()));
        dts.setIva(Integer.parseInt(jTFiva.getText()));
        dts.setSubtotal(Integer.parseInt(jTFsubtotal.getText()));
        dts.setTotalpago(Integer.parseInt(jTFtotalpago.getText()));
        dts.setDescri((jTFdescri.getText()));
        
        
        if (accion.equals("guardar")) {
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "La caja fue registrada satisfactoriamete!");
                mostrar("");
                inhabilitar();
                
                
                //Ocupamos la Habitaci√≥n Alquilada:
//                fhabitacion func3 = new fhabitacion();
//                vhabitacion dts3 = new vhabitacion();
//                
//                dts3.setIdhabitacion(Integer.parseInt(jTFidaccesorios.getText()));
//                func3.ocupar(dts3);
            }
        }
        else if (accion.equals("editar")) {
            dts.setIdCaja(Integer.parseInt(jTFidReserva.getText()));
            dts.setIdtipotrabajador(Integer.parseInt(jTFidTrabajador.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva fue editada satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

