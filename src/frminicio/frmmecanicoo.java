    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if(jTFestado.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre del Mecanico");
            jTFestado.requestFocus();
            return;
        }
        if(jTFapellido.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un apellido del Mecanico");
            jTFapellido.requestFocus();
            return;
        }
        if(jTFdireccion.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la direccecion del Mecanico");
            jTFdireccion.requestFocus();
            return;
        }
        

        vmecanico dts = new vmecanico();
        f_mecanico func = new f_mecanico();
        // f_modelo fun = new f_modelo();
       

        dts.setNombremeca(jTFestado.getText());
        dts.setApellidomeca(jTFapellido.getText());
        dts.setDireccionmeca(jTFdireccion.getText());
        

        if (accion.equals("guardar")) {
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El nombre de mecanico fue registrado satisfactoriamete!");
                mostrar("");
                inhabilitar();
            }
        }
        else if (accion.equals("editar")) {
            dts.setIdmecanico(Integer.parseInt(jTFidestado.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El nombre del mecanico fue editado satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

